# 2- Calculadora de Média Escolar 

# Notas dos alunos
nota1 =  7.5
nota2 =  8.0
nota3 = 6.5 

# Calcula a média somando as notas e dividindo pela quantidade de nottas (3)
media = (nota1 + nota2 + nota3) / 3

# Exibe as notas e o resultado final
print("Notas o estudante")
print(f"Nota 1: {nota1}")
print(f"Nota 2: {nota2}")
print(f"Nota 3: {nota3}")

print(f"Média Final: {media:.2f}")
print ("Média final:", round (media, 2))



